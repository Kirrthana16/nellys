"use strict";

// Select the form element
const reservationForm = document.querySelector(".reservation-form form");

// Function to handle form submission
const handleFormSubmit = function (event) {
  event.preventDefault(); // Prevent the default form submission

  // Get the reservation details from the form
  const reservationDate = event.target.querySelector("input[name='reservation-date']").value;
  const reservationTime = event.target.querySelector("select[name='reservation-time']").value;
  const numberOfGuests = event.target.querySelector("select[name='number-of-guests']").selectedOptions[0].textContent;

  // Create a popup message element
  const popupMessage = document.createElement("div");
  popupMessage.classList.add("popup-message");

  // Set the popup message content
  popupMessage.innerHTML = `
    <p>Your table has been successfully booked at Nelly's Damansara Jaya</p>
    <p>Reservation Details:</p>
    <p>Date: ${reservationDate}</p>
    <p>Time: ${reservationTime}</p>
    <p>Number of Guests: ${numberOfGuests}</p>
    <p>We look forward to welcoming you and providing an exceptional dining experience. If you have any special requests or need to make changes to your reservation, please contact us at 013-3804599.</p>
    <p>Thank you for choosing Nelly's Damansara Jaya!</p>
  `;

  // Append the popup message to the body
  document.body.appendChild(popupMessage);

  // Remove the popup message after 4 seconds
  setTimeout(() => {
    document.body.removeChild(popupMessage);
  }, 3000);
};

// Add the submit event listener to the form
if (reservationForm) {
  reservationForm.addEventListener("submit", handleFormSubmit);
} else {
  console.error("Reservation form not found");
}
